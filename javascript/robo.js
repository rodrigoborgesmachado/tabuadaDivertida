function Aleatorio(){
    var num1 = 0;
    while(num1 == 0 || num1 == 1 || num1 < 500){
        num1 = Math.floor(Math.random() * 1000);
    }

    return num1
}

function GetNome(){
    var num1 = -1;
    while(num1 >= 1500 || num1 == -1){
        num1 = Math.floor(Math.random() * 1500);
    }
    return listaNomes[num1].split(' ')[0].trim();
}

async function automatico(){
    while(true){
        // Multiplicação
        ComecaJogo();
        await sleep(500);
        document.getElementById('nome').value = GetNome();
        await sleep(500);
        SelecionaTipoJogo();
        await sleep(500);
        Multiplicacao();
        await sleep(500);

        for(var i = 1; i<= 20;i++){
            var temp = document.getElementById('lugarParaConta').innerHTML.split('<br>')[0];
            var temp2 = parseInt(temp.split('x')[0].trim()) * parseInt(temp.split('x')[1].trim());
            document.getElementById('valorResultado').value = temp2;
            
            await sleep(Aleatorio());
            InsereResposta(parseInt(temp.split('x')[0].trim()), parseInt(temp.split('x')[1].trim()), i);
            await sleep(Aleatorio());
        }

        // Adição
        ComecaJogo();
        await sleep(500);
        document.getElementById('nome').value = GetNome();
        await sleep(500);
        SelecionaTipoJogo();
        await sleep(500);
        Soma();
        await sleep(500);

        for(var i = 1; i<= 20;i++){
            var temp = document.getElementById('lugarParaConta').innerHTML.split('<br>')[0];
            var temp2 = parseInt(temp.split('+')[0].trim()) + parseInt(temp.split('+')[1].trim());
            document.getElementById('valorResultado').value = temp2;
            
            await sleep(Aleatorio());
            InsereResposta(parseInt(temp.split('+')[0].trim()), parseInt(temp.split('+')[1].trim()), i);
            await sleep(Aleatorio());
        }
        
        // Subtração
        ComecaJogo();
        await sleep(500);
        document.getElementById('nome').value = GetNome();
        await sleep(500);
        SelecionaTipoJogo();
        await sleep(500);
        Substracao();
        await sleep(500);

        for(var i = 1; i<= 20;i++){
            var temp = document.getElementById('lugarParaConta').innerHTML.split('<br>')[0];
            var temp2 = parseInt(temp.split('-')[0].trim()) - parseInt(temp.split('-')[1].trim());
            document.getElementById('valorResultado').value = temp2;
            await sleep(Aleatorio());
            InsereResposta(parseInt(temp.split('-')[0].trim()), parseInt(temp.split('-')[1].trim()), i);
            await sleep(Aleatorio());
        }
    }
}